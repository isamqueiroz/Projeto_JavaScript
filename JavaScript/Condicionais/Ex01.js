//01. Verificar se um número é positivo, negativo ou zero.

var prompt = require('prompt-sync')();

let numero = prompt ("Ola, digite um numero:")


if(numero =>1){
    console.log("O seu numero è positivo");
}else if(numero <= -1){
    console.log("O seu numero è positivo")
}else{
    console.log("O seu numero è zero")
}